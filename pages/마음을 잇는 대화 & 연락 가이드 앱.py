if search_keyword:
    matched_key = None
    for key in ADVICE_DATA.keys():
        if search_keyword in key or key in search_keyword:
            matched_key = key
            break
            
    if matched_key:
        data = ADVICE_DATA[matched_key]
        st.success(f"✨ '{search_keyword}'에 대한 따뜻한 솔루션입니다!")
        
        with st.container():
            st.subheader(data["title"])
            st.write(data["description"])
            
            st.markdown("📌 **이것만은 꼭 기억해 보세요!**")
            for tip in data["tips"]:
                st.markdown(f"- {tip}")
                
            st.markdown("---")
            st.info(data["example"])
    else:
        st.warning("😔 입력하신 키워드에 맞는 특별한 솔루션을 찾지 못했어요. 하지만 걱정 마세요!")
